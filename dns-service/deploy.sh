            if [ -n "$FAVICON_CONTENT" ]; then
                echo "  ,'favicon.ico': '$FAVICON_CONTENT'" >> src/static-assets.ts
            fi
            if [ -n "$LOGO_CONTENT" ]; then
                echo "  ,'logo.png': '$LOGO_CONTENT'" >> src/static-assets.ts
