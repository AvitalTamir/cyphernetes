NOTEBOOK_STATIC="../pkg/notebook/static"
if [ -d "$NOTEBOOK_STATIC" ]; then
    # Read the built index.html
    if [ -f "$NOTEBOOK_STATIC/index.html" ]; then
        echo "âœ¨ Updating static HTML with built assets..."
        
